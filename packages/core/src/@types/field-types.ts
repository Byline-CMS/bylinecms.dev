export type Field = StructureField | ValueField

export type FieldSet = readonly Field[]

export type ValueFieldType = ValueField['type']
export type StructureFieldType = StructureField['type']
export type FieldType = Field['type']

export type LocalizedField = { localized: true }
export type OptionalField = { optional: true }

// Type guards for field identification
export function isStructureField(field: Field): field is StructureField {
  return ['array', 'blocks', 'group'].includes(field.type)
}

export function isArrayField(field: Field): field is ArrayField {
  return field.type === 'array'
}

export function isBlocksField(field: Field): field is BlocksField {
  return field.type === 'blocks'
}

export function isGroupField(field: Field): field is GroupField {
  return field.type === 'group'
}

export function isValueField(field: Field): field is ValueField {
  return !isStructureField(field)
}

