export type StructureField = GroupField | ArrayField | BlocksField

export type ValueField =
  | TextField
  | TextAreaField
  | CheckboxField
  | BooleanField
  | SelectField
  | RichTextField
  | TimeField
  | DateField
  | DateTimeField
  | FloatField
  | IntegerField
  | DecimalField
  | JsonField
  | ObjectField
  | RelationField
